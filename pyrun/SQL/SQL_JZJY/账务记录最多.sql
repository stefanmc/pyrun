SELECT CUST_NO FROM KS.FUND_STOCK_CHANGE where CUST_NO not in ('00002303','00002306')
AND FUND_ACC_NO='01' AND CHANGE_TYPE > '00' AND ( CHANGE_TYPE >'50' OR CHANGE_TYPE< '50' OR (CHANGE_TYPE='50' AND BUSINESS_CODE='5394'))
AND (FUND_CHG<> 0 OR (SEC_CHG < -0.00001 OR SEC_CHG > 0.00001) ) AND DOUBLE_CHECK_FLAG='1' AND CANCEL_FLAG = '0'
AND OCCUR_DATE BETWEEN '20200601' AND '20210603'     
GROUP BY CUST_NO order by count(1) desc fetch first 1 rows ONLY;